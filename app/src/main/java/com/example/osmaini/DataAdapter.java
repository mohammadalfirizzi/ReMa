package com.example.osmaini;

public class DataAdapter {
    public String dokumentasi_gambar;
    public String dokumentasi_keterangan;
    public String dokumentasi_bidang_id;

    public String getDokumentasi_gambar() {
        return dokumentasi_gambar;
    }

    public void setDokumentasi_gambar(String dokumentasi_gambar) {
        this.dokumentasi_gambar = dokumentasi_gambar;
    }

    public String getDokumentasi_keterangan() {
        return dokumentasi_keterangan;
    }

    public void setDokumentasi_keterangan(String dokumentasi_keterangan) {
        this.dokumentasi_keterangan = dokumentasi_keterangan;
    }

    public String getDokumentasi_bidang_kode() {
        return dokumentasi_bidang_id;
    }

    public void setDokumentasi_bidang_kode(String dokumentasi_bidang_kode) {
        this.dokumentasi_bidang_id = dokumentasi_bidang_kode;
    }
}
